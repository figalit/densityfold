float eloop(int branches, int i[], int j[]){

	int s_size = 0;
	int  d_size;
	float part1;
	float part2;
	int index;

	d_size = branches;
	s_size = (i[1]-i[0]-1)+(j[0]-j[branches-1]-1);
	for(index = 1; index < branches; index++)
		s_size = s_size +(i[index+1]-j[index]-1);

	part1 = (float)a+(float)s_size*b+(float)d_size*c;

	part2 = 0;

	for(index = 1; index < branches; index++)
		if(j[index]+1 != i[index+1])
			if(j[index]+1 == i[index+1]-1){
				part2 = part2 + min(eDangle(i[index],j[index],j[index]+1),eDangle(i[index+1],j[index+1],i[index+1]-1 ));
			}
			else{
				part2 = part2 + eDangle(i[index],j[index],j[index]+1);
				part2 = part2 + eDangle(i[index+1],j[index+1],i[index+1]-1);
			}

	if(i[0]+1 != i[1])
		if(i[0]+1 == i[1]-1){
			part2 = part2 + min(eDangle(i[0],j[0],i[0]+1),eDangle(i[1],j[1],i[1]-1));
		}
		else{
			part2 = part2 + eDangle(i[0],j[0],i[0]+1);
			part2 = part2 + eDangle(i[1],j[1],i[1]-1);
		}


	if(j[branches-1]+1!=j[0])
		if(j[branches-1]+1 == j[0]-1){
			part2 = part2 + min(eDangle(i[branches-1],j[branches-1],j[branches-1]+1),eDangle(j[0],i[0],j[0]-1));
		}
		else{
			part2 = part2 + eDangle(i[branches-1],j[branches-1],j[branches-1]+1);
			part2 = part2 + eDangle(j[0],i[0],j[0]-1);
		}

	return part1+part2;
}

